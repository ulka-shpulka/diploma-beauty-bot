let slideIndex = 0;
const slides = document.querySelectorAll(".banner-slide");

function showSlides() {
    slides.forEach((slide, index) => {
        slide.style.opacity = "0";  // Скрываем слайды
        slide.style.display = "none";
    });

    slides[slideIndex].style.display = "block"; // Показываем текущий слайд
    setTimeout(() => {
        slides[slideIndex].style.opacity = "1"; // Добавляем плавность
    }, 50);

    slideIndex = (slideIndex + 1) % slides.length;
}

setInterval(showSlides, 3000);
showSlides(); // Показываем первый слайд сразу

// Функции для переключения слайдов при нажатии кнопок
function moveSlide(step) {
    slideIndex += step;

    if (slideIndex < 0) {
        slideIndex = slides.length - 1;
    } else if (slideIndex >= slides.length) {
        slideIndex = 0;
    }

    showSlides();
}

